//Program to represent objects
class Main
{
int age = 18;
String name = "Lakshmi";
void print()
{
System.out.println("Name : " + name + " age : " + age);
}
}
class ObjectCreation
{
public static void main(String args[])
{
Main obj = new Main();
obj.print();
}
}
//The above program is also an example of object oriented programing.